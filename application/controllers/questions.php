	<h1>Questions and Answers</h1>
	
	<br></br>
	
	<p><strong>Q - What is the purpose of this website?</strong></p>
	<p>A - The purpose of this website is to provide a space for teachers to upload videos, posts and comment on other posts via a forum.</p>
	<p><strong>Q - Who is this website designed for?</strong></p>
	<p>A - This website is designed for individuals in the academic field, this ranges from teacher assistants to senior lecturers.</p>
	<p><strong>Q - Is the content on this website moderated?</strong></p>
	<p>A - Yes, the content on this website is moderated by the forum moderators and the general website moderators.</p>
	<p><strong>Q - I made a mistake in my submission can I edit it? </strong></p>
	<p>A - Yes, you can edit your forum posts by accessing the post and clicking on the edit button located by the post.</p>
	<p><strong>Q - Is this website available in other languages?</strong></p>
	<p>A - No, this website is currently only available in the English language. </p>
	<p><strong>Q - What are the different subject threads?</strong></p>
	<p>A - Subject threads are there to split up the website into different sections making it easier to find what you are looking for.</p>
	<p><strong>Q - How many different subjects can I be involved in?</strong></p>
	<p>A - As many as you like, the forums are there to simply help browsing and finding what you want easier.</p>
	<p><strong>Q - What if moderators wrongfully abuse their power?</strong></p>
	<p>A - If you believe that a moderator has wrongfully abused their power you can appeal this via the report button located on the webpage.</p>
	<p><strong>Q - Is it ok to create multiple user accounts?</strong></p>
	<p>A - No, this is not permitted on the website and users caught using multiple accounts for whatever reason will have their account banned.</p>
	<p><strong>Q - Can I report content should I see it as inappropriate?</strong></p>
	<p>A - Absolutely, you can report the post via the button located on the webpage and if the post is deemed as inappropriate by a moderator action will be taken.</p>
	<P><strong>Q - Can I view posts without creating a user account?</strong></P>
	<p>A - Yes, although you will not be able to comment on threads without a user account.</p>
	
